package com.example.m213_tp_15_sharedpref;

public class Compte {
    private int id;
    private String nom;
    private String prenom;
    private String email;
    private String pass;

    public int getId() {
        return id;
    }

    public String getNom() {
        return nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public String getEmail() {
        return email;
    }

    public String getPass() {
        return pass;
    }
}
